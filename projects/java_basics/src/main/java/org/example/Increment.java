package org.example;

public class Increment {
    public static void main(String[] args){
        int a =5, b=5, c,d,e,f;
        c = a++;
        d = ++a;
        e = b--;
        f = --b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

    }
}
